    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String nome = nomeField.getText();
        double preco = Double.parseDouble(precoField.getText());
        int estoque = Integer.parseInt(estoqueField.getText());

        if (produto == null) {
            if (nome == null || nome.trim().isEmpty() || preco == 0 || estoque == 0) {
                JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }
                    
            controller.store(nome, preco, estoque);
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            controller.update(produto, nome, preco, estoque);
            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
        
        this.setVisible(false);
        ProdutoScreen.reload(new ProdutoScreen());
    }//GEN-LAST:event_saveBtnActionPerformed

