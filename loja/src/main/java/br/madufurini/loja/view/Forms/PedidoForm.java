    private void configurarClientes() {
        List<Cliente> clientes = clienteController.index();
        Cliente[] arrayClientes = clientes.toArray(new Cliente[0]);
        DefaultComboBoxModel clienteModel = new DefaultComboBoxModel<>(arrayClientes);
        clienteBox.setModel(clienteModel);
    }

    private void configurarProdutos() {
        List<Produto> produtos = produtoController.index();
        Produto[] arrayProdutos = produtos.toArray(new Produto[0]);
        DefaultComboBoxModel produtoModel = new DefaultComboBoxModel<>(arrayProdutos);
        produtoBox.setModel(produtoModel);

        produtoBox.addActionListener(e -> {
            Produto produtoSelecionado = (Produto) produtoBox.getSelectedItem();
            if (produtoSelecionado != null) {
                adicionarProdutoNaTabela(produtoSelecionado);
                produtoModel.removeElement(produtoSelecionado);
            }
        });
    }

    private void adicionarProdutoNaTabela(Produto produto) {
        DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
        tableModel.addRow(new Object[]{produto, produto.getPreco(), 1});
    }
