    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
        int rowCount = tableModel.getRowCount();

        List<ItensPedido> itens = new ArrayList<>();

        double valorTotal = 0;
        for (int i = 0; i < rowCount; i++) {
            Produto produto = (Produto) tableModel.getValueAt(i, 0);
            double preco = (Double) tableModel.getValueAt(i, 1);
            int quantidade = (Integer) tableModel.getValueAt(i, 2);

            ItensPedido item = new ItensPedido();
            item.setId_produto(produto.getId());
            item.setQuantidade(quantidade);
            item.setValor_unitario(preco);

            itens.add(item);
            valorTotal += (preco * quantidade);
        }
        
        Cliente clienteSelecionado = (Cliente) clienteBox.getSelectedItem();

       Pedido pedido = new Pedido();
       pedido.setId_cliente(clienteSelecionado.getId());
       pedido.setData_pedido(new Date());
       pedido.setItens(itens);
       pedido.setValor_total(valorTotal);
       
       pedidoController.store(pedido);
       
        JOptionPane.showMessageDialog(null, "Pedido realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false);
        PedidoScreen.reload(new PedidoScreen());
    }//GEN-LAST:event_saveBtnActionPerformed

