    private void clienteBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clienteBtn1ActionPerformed
        int linhaSelecionada = table.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela primeiro!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Produto produto = produtos.get(linhaSelecionada);
        ProdutoForm form = new ProdutoForm(produto);
        form.setVisible(true);
    }//GEN-LAST:event_clienteBtn1ActionPerformed

    private void clienteBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clienteBtn2ActionPerformed
        int linhaSelecionada = table.getSelectedRow();

        if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(null, "Selecione um item da tabela primeiro!", "Aviso", JOptionPane.WARNING_MESSAGE);
        return;
        }

        int resposta = JOptionPane.showConfirmDialog(
        null,
        "Tem certeza que deseja excluir este produto?",
        "Confirma√ß√£o",
        JOptionPane.YES_NO_OPTION
        );

        if (resposta == JOptionPane.YES_OPTION) {
            Produto produto = produtos.get(linhaSelecionada);
            controller.delete(produto);

            JOptionPane.showMessageDialog(null, "Produto exclu√≠do com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            ProdutoScreen.reload(new ProdutoScreen());
        }
    }//GEN-LAST:event_clienteBtn2ActionPerformed

